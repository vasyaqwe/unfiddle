export const formatDate = (dateStr: string, period: string) => {
   const date = new Date(dateStr)
   if (period === "1d") {
      return date.toLocaleDateString("en-US", {
         month: "short",
         day: "numeric",
      })
   }
   if (period === "1w") {
      return date.toLocaleDateString("en-US", {
         year: "numeric",
         month: "short",
         day: "numeric",
      })
   }
   if (period === "1m") {
      return date.toLocaleDateString("en-US", {
         year: "numeric",
         month: "short",
      })
   }
   if (period === "1y") {
      return date.toLocaleDateString("en-US", { year: "numeric" })
   }
   return dateStr
}
